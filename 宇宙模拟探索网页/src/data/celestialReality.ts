export const celestialRealityProfiles: CelestialRealityProfile[] = celestialRealityProfileInputs.map((profile) => ({
  ...profile,
  scaleProfile: getCelestialScaleProfile(profile.objectId),
}));

export function getCelestialRealityProfile(objectId: string): CelestialRealityProfile {
  const profile = celestialRealityProfiles.find((item) => item.objectId === objectId);
  if (!profile) {
    throw new Error(`Missing visible-light reality profile for catalog object "${objectId}".`);
  }
  return profile;
}
